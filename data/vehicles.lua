-- Função auxiliar para obter veículo por modelo
function VehicleData.GetVehicle(model)
    return VehicleData.Vehicles[model]
end

-- Função auxiliar para obter todos os veículos
function VehicleData.GetAllVehicles()
    return VehicleData.Vehicles
end

-- Função auxiliar para obter veículos por categoria
function VehicleData.GetVehiclesByCategory(category)
    local vehicles = {}
    for k, v in pairs(VehicleData.Vehicles) do
        if v.category == category then
            vehicles[k] = v
        end
    end
    return vehicles
end